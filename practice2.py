#first you check your git version in terminal with
print ("git --version")
#then make folder/directory with 
print("mkdir {foldername}")
#change path of terminal with
print("cd {foldername}")
#create code file with
print("code {filename}")
#initialize empty git repository in folder
print("git init")
#remember to save file in vscode before adding the file to the git with
print("git add {filename}")
#check status of git repository with
print("git status")